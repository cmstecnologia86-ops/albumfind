export function getStickerImagePath(
  teamCode: string,
  number: number,
): string | undefined {
  const normalizedTeamCode = teamCode.toUpperCase();
  const availableNumbers =
    stickerImageManifest[normalizedTeamCode];

  if (!availableNumbers?.includes(number)) {
    return undefined;
  }

  const paddedNumber = String(number).padStart(2, "0");

  return `/stickers/${normalizedTeamCode}/${normalizedTeamCode}-${paddedNumber}.webp`;
}
